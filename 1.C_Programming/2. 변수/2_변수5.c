// 4. 동일한 이름의 변수가 내부 범위에 정의된다면,
//    외부에 정의된 변수의 이름을 가립니다.
//    더 넓은 범위를 가지는 변수의 식별자와 동일한 이름의
//    변수를 정의하는 것은 안됩니다.

int main(void)
{
    int a = 100; // 1

    {
        int a = 200; // 2
        printf("%d\n", a); // 200
    } // 내부에 정의된 지역 변수는 더 이상 유효하지 않습니다.

    printf("%d\n", a); // 100

    return 0;
}